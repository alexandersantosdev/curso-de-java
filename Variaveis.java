public class Variaveis {

    //sintaxe para declaração de variávies em java:
    /*
    *<tipo> <nome> = <valor inicial> (opicional);
    *
    * ex:
    *
    * int idade = 25;
    * double altura = 1.68;
    * char sexo = 'f';
    *
    * uma variável possui um nome
    * um tipo
    * um valor
    * um endereço
    *
    * tipos primitivos:
    *
    * inteiros:
    *
    * byte = 8 bits valores de -128 a 127 -> valor padrão é o 0 zero
    * short = 16 bits valores de -32768 a 32767 -> valor padrão é o 0 zero
    * int = 32 bits valores de -214743648 a 214743647 -> valor padrão é o 0 zero
    * long = 64 bits valor padrão é 0L zero L
    *
    * ponto flutuante:
    *
    * float = 32 bits valor padrão é 0.0f zero.zero f
    * double = 64 bits valor padrão é 0.0 zero.zero
    *
    * caractere unicode
    *
    * char = 16 bits valor padrão é '\u0000'
    *
    * booleano = 1 bit valor padrão é false [1 = true / 0 = false]
    *
    * String = cadeia de caracteres (palavras ou textos)
    *
    * um bit pode armazenar 2 valores possíveis (0 ou 1)
    * cada bit = 2 possibilidades
    * 8 bits = 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 = 2 elevado a 8 = 256 possibilidades
    *
    *
    * nomes de variáveis iniciam com a letra minúscula
    * não se pode nomear uma variável com um número iniciando o nome : 5valor
    * mas pode existir número no nome da variável : valor5
    * pode usar o caracter [_] "underline" para nomes de variáveis: _nome
    * adota-se o padrão camel case em java: nomeDaVariavel
    * caracteres especiais e acentos não são permitidos em nomes de variáveis
    *
    * */
}
